//點擊相加
document.getElementById("increase").addEventListener('click', function(){
  document.getElementById("increase").innerText
= Number(document.getElementById("increase").innerText)+1})

//點擊相減
document.getElementById("decrease").addEventListener('click', function(){
  document.getElementById("decrease").innerText
= Number(document.getElementById("decrease").innerText)-1})